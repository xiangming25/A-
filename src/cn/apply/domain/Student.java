package cn.apply.domain;

public class Student {
	private Long id;
	private String XH;		//学号
	private String XM;		//学生姓名
	private String XY;		//学生姓名
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getXH() {
		return XH;
	}
	public void setXH(String xH) {
		XH = xH;
	}
	public String getXM() {
		return XM;
	}
	public void setXM(String xM) {
		XM = xM;
	}
	public String getXY() {
		return XY;
	}
	public void setXY(String xY) {
		XY = xY;
	}
	
	
}
